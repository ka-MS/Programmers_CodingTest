package Level0;

public class 가위바위보 {

	public static void main(String[] args) {
		// 가위는 2 바위는 0 보는 5로 표현합니다. 가위 바위 보를 내는 순서대로 나타낸 문자열 rsp가 매개변수로 주어질 때, rsp에 저장된
		// 가위 바위 보를 모두 이기는 경우를 순서대로 나타낸 문자열을 return하도록 solution 함수를 완성해보세요.
		String rsp = "205";
		String answer = "";
		for(int i = 0; i < rsp.length() ; i++){
           answer += solution(rsp.substring(i,i+1));
        }
		System.out.println(answer);
	}
	public static String solution(String rsp) {
		String result = rsp.equals("2") ? "0" : rsp.equals("5") ? "2" : rsp.equals("0") ? "5" : "";
		return result;
	}
}
